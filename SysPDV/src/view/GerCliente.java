    public void setRow() {

        while (tb1.getRowCount() > 0) {
            tb1.removeRow(0);
        }

        ArrayList<String[]> clientes = ps.buscar_amostra();

        clientes.forEach((cliente) -> {
            tb1.addRow(cliente);
        });

    }

