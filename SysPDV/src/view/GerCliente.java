    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        // faz busca de resultados na base
        if (_search.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você está fazendo uma busca vazia!", null, JOptionPane.WARNING_MESSAGE);
        } else {
            
            // limpa a tabela
            while (tb1.getRowCount() > 0) {
                tb1.removeRow(0);
            }

            ps.busca_cliente(_search.getText()).forEach((cliente) -> {
                tb1.addRow(cliente);
            });

        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void _fornecedoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event__fornecedoresActionPerformed
        // TODO add your handling code here:

        if (_fornecedores.isSelected()) {

            while (tb1.getRowCount() > 0) {
                tb1.removeRow(0);
            }

            ArrayList<String[]> fornecedores = fn.buscar_amostra();

            fornecedores.forEach((fornecedor) -> {
                tb1.addRow(fornecedor);
            });

        } else {
            setRow();
        }
    }//GEN-LAST:event__fornecedoresActionPerformed

